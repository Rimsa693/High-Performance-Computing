/******************************************************************************
  Displays two grey scale images. On the left is an image that has come from an 
  image processing pipeline, just after colour thresholding. On the right is 
  the result of applying an edge detection convolution operator to the left
  image. This program performs that convolution.
   
  Things to note:
    - A single unsigned char stores a pixel intensity value. 0 is black, 256 is
      white.
    - The colour mode used is GL_LUMINANCE. This uses a single number to 
      represent a pixel's intensity. In this case we want 256 shades of grey,
      which is best stored in eight bits, so GL_UNSIGNED_BYTE is specified as 
      the pixel data type.
    
  To compile adapt the code below wo match your filenames:  
    cc -o ip_coursework ip_coursework.c -lglut -lGL -lm 
   
  Dr Kevan Buckley, University of Wolverhampton, 2018
******************************************************************************/
#define w 100 
#define h 72

unsigned char picture[], outcome[w * h];

int timedifference(struct timespec *start, 
		   struct timespec *finish, 
long long int *difference) {
long long int dsecs = finish->tv_sec - start->tv_sec;
long long int dnanosecs = finish->tv_nsec - start->tv_nsec;

if(dnanosecs < 0) {
  dsecs--;
  dnanosecs += 1000000000; 
}

*difference = dsecs * 1000000000 + dnanosecs;
return !(*difference > 0);
}

void edge(unsigned char *in, unsigned char *out) {
  int g;
  int n_pixels = w * h;

  for(g=0;g<n_pixels;g++) {
    int x, y; // the pixel of interest
    int a, b, c, d; // the pixels adjacent to x,y used for the calculation
    int r; // the result of calculate
    
    y = g / w;
    x = g - (w * y);

    if (x == 0 || y == 0 || x == w - 1 || y == h - 1) {
      outcome[g] = 0;
    } else {
      a = g + w;
      b = g - 1;
      c = g + 1;
      d = g - w;

      r = (in[g] * 4) + (in[a] * -1) + (in[b] * -1) + (in[c] * -1)
          + (in[d] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        out[g] = 255;
      } else {
        out[g] = 0;
      }
    }
  }
}

void tidy_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(w, h, GL_LUMINANCE, GL_UNSIGNED_BYTE, picture);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(w, h, GL_LUMINANCE, GL_UNSIGNED_BYTE, outcome);
  glFlush();
}

static void key_pressed(unsigned char key, int x, int y) {
  switch(key){
    case 27: // escape
      tidy_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}

int main(int argc, char **argv) {
  signal(SIGINT, sigint_callback);
  struct timespec start, finish;
  long long int timeelapsed;


  clock_gettime(CLOCK_MONOTONIC, &start);	
  signal(SIGINT, sigint_callback);
 
  printf("image dimensions %dx%d\n", w, h);

  edge(picture, outcome);
  clock_gettime(CLOCK_MONOTONIC, &finish);
  timedifference(&start, &finish, &timeelapsed);
  printf("The time taken is: %lldns or %0.9lfs\n", timeelapsed,  (timeelapsed/1.0e9));

  glutInit(&argc, argv);
  glutInitWindowSize(w * 2,h);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
      
  glutCreateWindow("6CS005 Image Progessing Courework");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0); 

  glutMainLoop(); 

  tidy_and_exit();
  
  return 0;
}

